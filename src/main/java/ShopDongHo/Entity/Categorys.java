package ShopDongHo.Entity;

public class Categorys {
	private int malsp;
	private String tenlsp;
	private String mota;
	public Categorys() {
		super();
	}
	public int getMalsp() {
		return malsp;
	}
	public void setMalsp(int malsp) {
		this.malsp = malsp;
	}
	public String getTenlsp() {
		return tenlsp;
	}
	public void setTenlsp(String tenlsp) {
		this.tenlsp = tenlsp;
	}
	public String getMota() {
		return mota;
	}
	public void setMota(String mota) {
		this.mota = mota;
	}
	
	
	
}
